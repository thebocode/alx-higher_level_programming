#!/usr/bin/python3
def add(a, b):
    """My addition function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        The return value of (a + b)
    """
    return (a + b)

def sub(a, b):
    """My subtraction function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        The return value of (a - b)
    """
    return (a - b)

def mul(a, b):
    """My multiplication function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        The return value of (a * b)
    """
    return (a * b)

def div(a, b):
    """My division function

    Arguments:
        a: first integer
        b: second integer

    Returns:
        The return value of (a / b)
    """
    return int(a / b)
